class Solution:
    def getSum(self, a: int, b: int) -> int:
        mask32 = 0xFFFFFFFF  # Маска для ограничения до 32 бит
        
        # Шаг 1: Превращаем знаковые числа в их 32-битный эквивалент (дополнение до двух)
        a = a & mask32
        b = b & mask32

        res = 0
        carry = 0

        # Для 32-битных чисел цикл должен идти ровно 32 раза
        for pos in range(32):
            # Извлекаем самый правый бит (LSB) вместо постоянного сдвига маски
            a_bit = a & 1
            b_bit = b & 1

            # Логика полного сумматора (Full Adder)
            x = a_bit ^ b_bit ^ carry
            
            # Исправлено: используем побитовые операторы & и |, так как and/or работают иначе
            carry = (a_bit & b_bit) | (a_bit & carry) | (b_bit & carry)

            # Формируем итоговое число, сдвигая полученный бит на нужную позицию
            res |= (x << pos)

            # Сдвигаем числа вправо, чтобы на следующей итерации проверить новый бит
            a >>= 1
            b >>= 1

        # Шаг 2: Если 31-й бит (знаковый) равен 1, число отрицательное
        # Возвращаем его обратно в знаковое представление Python
        if res & 0x80000000:
            return ~(res ^ mask32)
        
        return res
